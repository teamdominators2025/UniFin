document.addEventListener("DOMContentLoaded", () => {
  // 3.1 Restore language & Setup Listener
  const savedLang = localStorage.getItem("preferredLang") || "en";
  const langSelect = document.getElementById("langSelect");
  
  if (langSelect) {
    langSelect.value = savedLang;
    
    // Run translation immediately on load
    changeLanguage();

    // IMPORTANT: Listen for changes on the dropdown menu
    langSelect.addEventListener("change", () => {
      changeLanguage();
    });
  }

  // 3.2 Initialize Visuals
  initThreeJS();
  initParallax();

  // 3.3 Reveal animation
  const observer = new IntersectionObserver(
    entries => {
      entries.forEach(e => {
        if (e.isIntersecting) e.target.classList.add("show-element");
      });
    },
    { threshold: 0.1 }
  );

  document.querySelectorAll(".hidden-element").forEach(el => observer.observe(el));

  // 3.4 Mobile menu
  const hamburger = document.querySelector(".hamburger");
  const navList = document.querySelector(".nav-list");

  if (hamburger && navList) {
    hamburger.addEventListener("click", () => {
      navList.classList.toggle("active");
    });
  }

  // 3.5 Smooth scrolling
  document.querySelectorAll(".nav-link").forEach(link => {
    link.addEventListener("click", e => {
      const href = link.getAttribute("href");
      if (href?.startsWith("#")) {
        e.preventDefault();
        const target = document.querySelector(href);
        if (target) {
          window.scrollTo({
            top: target.offsetTop - 80,
            behavior: "smooth"
          });
        }
      }
    });
  });
});

